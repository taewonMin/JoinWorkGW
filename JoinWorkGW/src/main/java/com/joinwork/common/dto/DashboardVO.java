package com.joinwork.common.dto;

public class DashboardVO {
	private String empId;
	private String dashboardDuty;
	private String dashboardIssue;
	private String dashboardProject;
	private String dashboardApproval;
	private String dashboardReservation;
	private String dashboardBoard;
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getDashboardDuty() {
		return dashboardDuty;
	}
	public void setDashboardDuty(String dashboardDuty) {
		this.dashboardDuty = dashboardDuty;
	}
	public String getDashboardIssue() {
		return dashboardIssue;
	}
	public void setDashboardIssue(String dashboardIssue) {
		this.dashboardIssue = dashboardIssue;
	}
	public String getDashboardProject() {
		return dashboardProject;
	}
	public void setDashboardProject(String dashboardProject) {
		this.dashboardProject = dashboardProject;
	}
	public String getDashboardApproval() {
		return dashboardApproval;
	}
	public void setDashboardApproval(String dashboardApproval) {
		this.dashboardApproval = dashboardApproval;
	}
	public String getDashboardReservation() {
		return dashboardReservation;
	}
	public void setDashboardReservation(String dashboardReservation) {
		this.dashboardReservation = dashboardReservation;
	}
	public String getDashboardBoard() {
		return dashboardBoard;
	}
	public void setDashboardBoard(String dashboardBoard) {
		this.dashboardBoard = dashboardBoard;
	}
	
}
